    private void jcb_departamentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcb_departamentosActionPerformed
        // TODO add your handling code here:
        try{
            trab.contrato.buscaSobreDepartamento(jt_datosContratos, jcb_departamentos.getSelectedItem().toString());
            jrb_departamento.setSelected(true);
            jtf_trabajador.setText("");
        }catch(NullPointerException x){
            
        }
    }//GEN-LAST:event_jcb_departamentosActionPerformed

