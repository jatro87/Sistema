    private void jrb_verTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jrb_verTodosActionPerformed
        // TODO add your handling code here:
        jcb_categorias.setSelectedItem("Selecciona");
        jcb_departamentos.setSelectedItem("Selecciona");
        jtf_trabajador.setText("");
        trab.contrato.getContratos(jt_datosContratos);
        jrb_verTodos.setSelected(true);
        jrb_verTodos.requestFocus();
    }//GEN-LAST:event_jrb_verTodosActionPerformed

